// Operator => Division & Remainder
const n = 4598;

const digit = n / 1000;
console.info(digit); // return : 4.598

let r = n % 1000;
console.info(r); // return : 598

const digit1 = parseInt(n / 1000)
console.info(digit1); // return : 4

let r1 = parseInt(n % 1000);
console.info(r1) // return : 598