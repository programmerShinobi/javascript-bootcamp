// string
const s = "hello";
console.info(typeof s); // output : string

// number
const n = 12;
console.info(typeof n); // output : number

// boolean
const result = true;
console.info(typeof result); // output : boolean

// object null
const nullvar = null;
console.info(typeof nullvar); // output : object

// undefined
let blank;
console.info(typeof blank); // output : undefined

