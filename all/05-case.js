/**
 * Case
 * ---> Sebuah konsep yang mengacu pada cara penulisan nama variabel, function, atau konstanta
 *      dalam kode program, terutama dalam bahasa pemrograman yang menggunakan kapitalisasi
 * 
 * Jenis case yang umum digunakan dalam pemrograman :
 * 1.   Uppercase 
 * 2.   CamelCase 
 * 3.   CamelCase 
 * 4.   PascalCase 
 * 
 */

/**
 * Uppercase adalah cara penulisan nama
 * dengan menggunakan huruf kapital untuk semua huruf
 * 
 */
const MY_VARIABLE = "value";
function MY_FUNCTION() {
    // ...
}
const PI = 3.14;

/**
 * CamelCase adalah cara penulisan nama
 * dengan menggunakan huruf kapital pada kata pertama,
 * dan huruf kecil pada kata selanjutnya.
 * CamelCase biasa digunakan untuk nama variabel
 * atau function yang terdiri dari lebih dari satu kata
 * 
 */
const myVariable = "value";
function calculateDiscount(price, discount) {
    // ...
}

/**
 * snake_case adalah cara penulisan nama
 * dengan menggunakan garis bawah sebagai pemisah antar kata,
 * dan menggunakan huruf kecil untuk semua huruf.
 * Snake_case biasa digunakan untuk nama variabel
 * atau function yang terdiri dari lebih dari satu kata.
 * 
 */
const user_age = 30;
function calculate_discount(price, discount) {
    // ...
}

/**
 * PascalCase adalah cara penulisan nama yang mirip
 * dengan CamelCase, namun menggunakan huruf kapital untuk semua kata.
 * PascalCase biasa digunakan untuk nama class atau konstruktor. 
 */
class User {
    // ...
}

function Student(name, age) {
    // ...
}

