/**
 * Comments
 * ---> Teks yang tidak diinterpretasikan oleh interpreter JavaScript sebagai bagian dari kode program.
 *      Comments digunakan untuk memberikan dokumentasi dan keterangan tambahan dalam kode program
 *      yang dapat membantu orang lain yang membaca kode tersebut untuk memahami apa yang sedang terjadI
 */

/**
 * Single-line comments
 * ---> Comments yang hanya terdiri dari satu baris
 *      dan ditandai dengan tanda slash-ganda (//) di awal baris
 */
let x = 10; // this is also a single-line comment

/**
 * Multi-line comments
 * ---> Comments yang dapat terdiri dari beberapa baris
 *      dan ditandai dengan tanda slash-ganda /* di awal blok comments
 *      dan tanda asterisk-ganda */
/**    di akhir blok
*/

/*
This is a multi-line comment.
It can span multiple lines and is often used
to provide more detailed documentation or explanations
in the code.
*/
let y = 20;
