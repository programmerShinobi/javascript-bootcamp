// array
const arr = [1, 2, 3];
console.info(typeof arr); // output : object

// object
const obj = {
    nama: "js",
    ipk: 3.45
};
console.info(typeof obj); // output : object

// function
const fnc = function () {
    return "hello"
};
console.info(typeof fnc); // output : function

// object date
const tgl = new Date();
console.info(typeof tgl); // output : object

// creating symbol
const x = Symbol();
console.info(typeof x); // output : symbol

